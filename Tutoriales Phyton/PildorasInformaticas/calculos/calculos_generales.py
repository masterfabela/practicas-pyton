def sumar(operador1, operador2):
    print("El resultado de la suma es: ", operador1+operador2)


def restar(operador1, operador2):
    print("El resultado de la resta es: ", operador1-operador2)


def multiplicar(operador1, operador2):
    print("El resultado de la multiplicacion es: ", operador1*operador2)


def dividir(dividendo, divisor):
    print("El resultado de la suma es: ", dividendo/divisor)


def potencia(base, exponente):
    print("El resultado de la potencia es: ", base**exponente)


def redondear(numero):
    print("El numero redondeado es: ", round(numero))
